package fr.digi.m052024.ex1;

/**
 * @author fabriceM
 */
public class Calcul {

    /**
     * @param a premier paramètre de l'addition
     * @param b second paramètre de l'addition
     * @return ajoute a et b
     */
    public int ajouter(int a, int b) {
        return a + b;
    }

    /**
     * @param a premier paramètre de la soustraction
     * @param b second
     * @return soustrait b à a
     */
    public int soustraire(int a, int b) {
        return a - b;
    }
}
